			<legend>Photographer</legend>
			<p>Welcome to my website! I love taking photos, from landscapes to animals, all around the world.</p>
			<p>I first became interested in photography during family vacations. My dad would take out his camera and take pictures of us or our surroundings and I couldn't help but be amazed that a piece of machinery could capture what we saw.</p>
			<p>As I grew older, I would use a Canon PowerShot A75 (an old point and shoot) to take photos of my friends. Eventually I started using it to photograph strangers, architecture, and landscapes. Photography was calling - I knew it was time to graduate to a more professional camera.</p>
			<p>Now I dedicate much of my time to taking photos - planning my shoots, experimenting with different equipment and just having fun. I hope you enjoy the selection I've put online, and leave some comments if you'd like to let me know what you think!</p>
